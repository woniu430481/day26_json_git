package junit;

public class Calculator {
    /**
     * 两个数的和
     * @param a
     * @param b
     * @return
     */
    public int add(int a,int b){

        return a+b;

    }

    /**
     * 求两个数的差
     * @param a
     * @param b
     * @return
     */
    public int sub(int a,int b){

        return a-b;

    }
}
