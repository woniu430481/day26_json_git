package com.qf.junit;

public class Calculator {

    /**
     * 计算两个数的和
     * @param a
     * @param b
     * @return
     */
    public int add(int a,int b){
        return a+b;
    }


    /**
     * 计算两个数的差
     * @param a
     * @param b
     * @return
     */
    public int sub(int a,int b){
        return a-b;
    }
}
